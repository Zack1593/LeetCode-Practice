package LinkedList;

public class ListNode {

    public static void main(String[] args) {
        System.out.println("test");
    }
    // Definition for singly-linked list.
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
